package trainingPackage;

public class Package31 {

	public static void main(String[] args) {
		int a, b, c;
		a = 67;
		b = 78;

		// SWAPPING A AND B VALUE

		c = a; // assigning 'a' value to 'c' for getting it again
		a = b; // assigning b value to 'a'.so 'a' got 'b' value
		b = c; // again getting initial 'a' value from 'c' and assign in b

		System.out.println(a);
		System.out.println(b);

	}

}
